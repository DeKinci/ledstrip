#!/usr/bin/env node
// Compress web resources and generate C++ PROGMEM headers.
// Reads a device's resources.txt manifest, generates headers in that device's src/gen/.
//
// Usage: npx tsx infra/build/src/compress-resources.ts <device-dir>
// Example: npx tsx infra/build/src/compress-resources.ts device/ledstrip

import fs from 'fs'
import path from 'path'
import crypto from 'crypto'
import { ROOT } from './paths.js'

const deviceDir = process.argv[2]
if (!deviceDir) {
  console.error('Usage: compress-resources.ts <device-dir>')
  process.exit(1)
}

const manifestPath = path.join(ROOT, deviceDir, 'resources.txt')
const outputDir = path.join(ROOT, deviceDir, 'src', 'gen')

if (!fs.existsSync(manifestPath)) {
  console.error(`No resources.txt found at ${manifestPath}`)
  process.exit(1)
}

fs.mkdirSync(outputDir, { recursive: true })

interface Resource {
  sourcePath: string
  processing: 'original' | 'brotli'
  varName: string
  outputPath: string
}

function sanitizeName(filePath: string): string {
  return path.basename(filePath).replace(/[.\-]/g, '_')
}

function sha256(filePath: string): string {
  const data = fs.readFileSync(filePath)
  return crypto.createHash('sha256').update(data).digest('hex')
}

function readStoredHash(headerPath: string): string | null {
  if (!fs.existsSync(headerPath)) return null
  const first = fs.readFileSync(headerPath, 'utf-8').split('\n')[0]
  const match = first.match(/Hash:\s*(\w+)/)
  return match ? match[1] : null
}

function generateHeader(data: Buffer, varName: string, sourcePath: string, hash: string): string {
  const guard = `RSC_${varName.toUpperCase()}_H`
  const size = data.length

  let hex = ''
  for (let i = 0; i < data.length; i += 16) {
    const chunk = data.subarray(i, Math.min(i + 16, data.length))
    const line = Array.from(chunk).map(b => `0x${b.toString(16).padStart(2, '0')}`).join(', ')
    hex += `    ${line}${i + 16 < data.length ? ',' : ''}\n`
  }

  return `// Hash: ${hash}
// Generated from: ${sourcePath}
// Size: ${size.toLocaleString()} bytes
// DO NOT EDIT - Auto-generated by infra/build/src/compress-resources.ts

#ifndef ${guard}
#define ${guard}

#include <Arduino.h>

const uint8_t ${varName}[] PROGMEM = {
${hex}};

const size_t ${varName}_len = ${size};

#endif // ${guard}
`
}

// Parse manifest
const manifest = fs.readFileSync(manifestPath, 'utf-8')
const resources: Resource[] = []

for (const line of manifest.split('\n')) {
  const trimmed = line.trim()
  if (!trimmed || trimmed.startsWith('#')) continue

  const parts = trimmed.split(/\s+/)
  if (parts.length !== 2) continue

  const [sourcePath, processing] = parts
  if (processing !== 'original' && processing !== 'brotli') continue

  const varName = sanitizeName(sourcePath)
  const outputPath = path.join(outputDir, `w_${varName}.h`)

  resources.push({ sourcePath, processing: processing as any, varName, outputPath })
}

console.log(`Processing ${resources.length} resources → ${outputDir}`)

let regenerated = 0
for (const res of resources) {
  const fullSource = path.join(ROOT, res.sourcePath)

  if (!fs.existsSync(fullSource)) {
    console.log(`  ⚠ Missing: ${res.sourcePath}`)
    continue
  }

  const hash = sha256(fullSource)
  const storedHash = readStoredHash(res.outputPath)

  if (hash === storedHash) {
    continue
  }

  const data = fs.readFileSync(fullSource)
  // TODO: brotli compression support (for now, all original)
  const header = generateHeader(data, res.varName, res.sourcePath, hash)
  fs.writeFileSync(res.outputPath, header)
  console.log(`  → w_${res.varName}.h (${data.length.toLocaleString()} bytes)`)
  regenerated++
}

if (regenerated > 0) {
  console.log(`Regenerated ${regenerated} header(s)`)
} else {
  console.log('All headers up to date')
}
